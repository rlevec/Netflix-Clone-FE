import { apiSlice } from "../../Slices/apiSlice"
import { routes } from "../../../Routes/routes"


const extendedRootApi = apiSlice.injectEndpoints({
  endpoints: (builder) => ({
    postChangeUserActivePlan: builder.mutation({
    query: (payload) => ({
        url: routes?.serverRoutes?.changeActivePlanRoute,
        method: "POST",
        body: {payload},
      }),
      invalidatesTags: ["UsersData"]
    }),
  }),
  overrideExisting: false,
})

export const { usePostChangeUserActivePlanMutation } = extendedRootApi